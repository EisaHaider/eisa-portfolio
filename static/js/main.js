function updateTexts(lang) {
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.dataset.i18n;
        if (translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    document.querySelectorAll("[data-i18n-url]").forEach(el => {
        const key = el.dataset.i18nUrl;
        const urlKey = `${key}_url`;
        if (translations[lang][urlKey]) {
            el.href = translations[lang][urlKey];
        }
    });

    if (typeof lucide !== "undefined") {
        lucide.createIcons();
    }
}

function applyLanguage(lang) {
    localStorage.setItem("language", lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = translations[lang].direction;
    updateTexts(lang);
}

function toggleLanguage() {
    const current = localStorage.getItem("language") || "en";
    const next = current === "en" ? "ar" : "en";
    applyLanguage(next);
}

function applyTheme(theme) {
    localStorage.setItem("theme", theme);
    if (theme === "light") {
        document.documentElement.classList.add("light");
    } else {
        document.documentElement.classList.remove("light");
    }
}

function toggleTheme() {
    const current = localStorage.getItem("theme") || "dark";
    const next = current === "dark" ? "light" : "dark";
    applyTheme(next);
}

function revealElements() {
    const elements = document.querySelectorAll(".fade-up");
    const observer = new IntersectionObserver(
        entries => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add("show");
                }
            });
        },
        { threshold: 0.1 }
    );

    elements.forEach(el => {
        observer.observe(el);
    });
}

document.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem("language") || "en";
    const savedTheme = localStorage.getItem("theme") || "dark";

    applyLanguage(savedLang);
    applyTheme(savedTheme);

    const langBtn = document.getElementById("language-toggle");
    if (langBtn) {
        langBtn.addEventListener("click", toggleLanguage);
    }

    const themeBtn = document.getElementById("theme-toggle");
    if (themeBtn) {
        themeBtn.addEventListener("click", toggleTheme);
    }

    if (typeof lucide !== "undefined") {
        lucide.createIcons();
    }

    revealElements();
});
